int	ft_sqrt(int nb)
{
	int	i;
	int	total;

	i = 1;
	total = 0;
	if (nb <= 0)
		return (0);
	while (nb > 0)
	{
		nb -= i;
		i += 2;
		total++;
	}
	if (nb < 0)
		return (0);
	return (total);
}
